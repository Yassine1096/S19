#include <unistd.h>

void	ft_putstr(char	*str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char	**argv)
{
	int	j;

	j = argc - 1;
	while (1 <= j)
	{
		ft_putstr(argv[j]);
		ft_putstr("\n");
		j--;
	}
	if (argc < 2)
		return (0);
}
